// CDFMApp 初始化

BOOL CDFMApp::InitInstance()
{
	// 如果一个运行在 Windows XP 上的应用程序清单指定要
	// 使用 ComCtl32.dll 版本 6 或更高版本来启用可视化方式，
	//则需要 InitCommonControlsEx()。否则，将无法创建窗口。
	INITCOMMONCONTROLSEX InitCtrls;
	InitCtrls.dwSize = sizeof(InitCtrls);
	// 将它设置为包括所有要在应用程序中使用的
	// 公共控件类。
	InitCtrls.dwICC = ICC_WIN95_CLASSES;
	InitCommonControlsEx(&InitCtrls);


	//ado连接access数据库
	::CoInitialize(NULL);
	HRESULT hr;
	try{
		hr = m_pConnect.CreateInstance("ADODB.Connection");
		if (SUCCEEDED(hr))
		{
			m_pConnect->ConnectionTimeout = 3;
			hr = m_pConnect->Open("Provider=Microsoft.ACE.OLEDB.12.0;Data Source=DFM.accdb", "", "", adModeUnknown);
		}
	}
	catch (_com_error e)
	{
		CString temp;
		//temp.Format(wchar_t("连接数据库错误信息:%s"),e.ErrorMessage());
		//LPCWSTR temp2 =&wchar_t("提示信息")
		//::MessageBox(NULL, temp, CString("提示信息"), NULL);
		MessageBox(NULL, CString("数据文件改变！请检查数据文件的路径和文件名\n错误信息: ")+e.ErrorMessage(), CString("系统提示"), MB_OK | MB_ICONEXCLAMATION);
		return false;
	}

	CWinApp::InitInstance();


	AfxEnableControlContainer();

	// 创建 shell 管理器，以防对话框包含
	// 任何 shell 树视图控件或 shell 列表视图控件。
	CShellManager *pShellManager = new CShellManager;

	// 标准初始化
	// 如果未使用这些功能并希望减小
	// 最终可执行文件的大小，则应移除下列
	// 不需要的特定初始化例程
	// 更改用于存储设置的注册表项
	// TODO: 应适当修改该字符串，
	// 例如修改为公司或组织名
	SetRegistryKey(_T("应用程序向导生成的本地应用程序"));

	CDFMDlg dlg;
	m_pMainWnd = &dlg;
	INT_PTR nResponse = dlg.DoModal();
	if (nResponse == IDOK)
	{
		// TODO: 在此放置处理何时用
		//  “确定”来关闭对话框的代码
	}
	else if (nResponse == IDCANCEL)
	{
		// TODO: 在此放置处理何时用
		//  “取消”来关闭对话框的代码
	}
	else if (nResponse == -1)
	{
		TRACE(traceAppMsg, 0, "警告: 对话框创建失败，应用程序将意外终止。\n");
		TRACE(traceAppMsg, 0, "警告: 如果您在对话框上使用 MFC 控件，则无法 #define _AFX_NO_MFC_CONTROLS_IN_DIALOGS。\n");
	}
	// 删除上面创建的 shell 管理器。
	if (pShellManager != NULL)
	{
		delete pShellManager;
	}

	// 由于对话框已关闭，所以将返回 FALSE 以便退出应用程序，
	//  而不是启动应用程序的消息泵。
	return FALSE;
}

